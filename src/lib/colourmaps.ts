export function buildMapLibreColours(
    min: number,
    max: number,
    colourmap: string = "davos",
): (string | number)[] {
    let result: (string | number)[] = [];
    const cm: number[][] = colourmaps[colourmap as keyof typeof colourmaps];
    for (let i = 0; i < 256; i++) {
        let e = min + (i / 255) * (max - min);
        result.push(e);
        result.push(
            `rgb(${cm[i][0] * 255}, ${cm[i][1] * 255}, ${cm[i][2] * 255})`,
        );
    }
    return result;
}

export function interpolateColour(
    colourmap: string = "davos",
    normalizedValue: number,
): number[] {
    const cm: number[][] = colourmaps[colourmap as keyof typeof colourmaps];
    const i = (cm.length - 1) * normalizedValue;
    const i_low = Math.floor(i);
    const i_high = Math.ceil(i);
    if (i_low == i_high) {
        return cm[i_low];
    } else {
        function interp(y1: number, y2: number, i: number): number {
            return ((y2 - y1) / (i_high - i_low)) * (i - i_low) + y1;
        }
        return [
            interp(cm[i_low][0], cm[i_high][0], i),
            interp(cm[i_low][1], cm[i_high][1], i),
            interp(cm[i_low][2], cm[i_high][2], i),
        ];
    }
}

